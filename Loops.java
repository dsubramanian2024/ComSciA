public class Loops
{
  public static void main(String[] args)
  {
    //create two variables for while Loops
    int ex1Count = 1;
    int ex2Count = 0;

    System.out.println("Result of a while loop using unit index: ");

    while (ex1Count <= 10)
    {
      System.out.println(ex1Count);
      ex1Count++;
    }

    System.out.println("Result of a while loop using zero index: ");

    while (ex2Count <= 10)
    {
      System.out.println(ex2Count);
      ex2Count++;
    }
  }
}
